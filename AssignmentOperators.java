public class AssignmentOperators {
    public static void main(String[] args) {
        //! using assignment operators
        int a=20;
        a += 10; // a = a + 10
        System.out.println(a);
        a -= 10; // a = a - 10
        System.out.println(a);
        a *= 10; // a = a * 10
        System.out.println(a);
        a /= 10; // a = a / 10
        System.out.println(a);
        a %= 10; // a = a % 10
        System.out.println(a);

        System.out.println(a); // Output: 30






    
}
}
