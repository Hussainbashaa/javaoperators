
public class AthematicOpertaors {

    public static void main(String[] args) {
        int num1 = 30;
        int num2 = 10;
        //!addition
        System.out.println(num1 + num2);
        //!subtraction
        System.out.println(num1 - num2);
        //!multiplication
        System.out.println(num1 * num2);
        //!division
        System.out.println(num1 / num2);
        //!modulus
        System.out.println(num1 % num2);
        //!concatenation
        System.out.println("Result:" + num1 + num2);
        //!double addition
        double num3 = 30.0;
        double num4 = 4.0;
        System.out.println(num3 + num4);
        //!double multiplication
        System.out.println(num3 * num4);
        //!double subtraction
        System.out.println(num3 - num4);
        //!double division
        System.out.println(num3 / num4);
        //!double modulus
        System.out.println(num3 % num4);

    }

}
