
public class RelationalOperators {

    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;
        //! >
        System.out.println(num1 > num2);
        //! <
        System.out.println(num1 < num2);
        //! >=
        System.out.println(num1 >= num2);
        //! <=
        System.out.println(num1 <= num2);
        //! ==
        System.out.println(num1 == num2);
        //! !=
        System.out.println(num1 != num2);
        //! using characters
        char ch1 = 'A';
        char ch2 = 'B';
        System.out.println("ch1 > ch2 :" + (ch1 > ch2));
        System.out.println("ch1 < ch2 :" + (ch1 < ch2));
        System.out.println("ch1 < ch2 :" + (ch1 == ch2));
        System.out.println("ch1 < ch2 :" + (ch1 != ch2));
        //? System.out.println(ch1&= ch2); 

    }

}
